create table seller(
                       seller_id bigint primary key,
                       seller_address varchar(255),
                       seller_address_detail varchar(255),
                       seller_business_number varchar(255),
                       seller_email varchar(255),
                       seller_is_active varchar(255),
                       seller_name varchar(255),
                       seller_password varchar(255),
                       seller_phone varchar(255),
                       seller_register_date varchar(255),
                       seller_zipcode varchar(255),
                       seller_shop_name varchar(255)
);